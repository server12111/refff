def build_withdrawal_msg(withdrawal_id: int, username: str, user_id: int, amount: float, status: str) -> str:
    status_map = {
        "pending":  "‚è≥ –°—Ç–∞—Ç—É—Å: –Ω–∞ —Ä–∞—Å—Å–º–æ—Ç—Ä–µ–Ω–∏–∏",
        "approved": "‚úÖ –°—Ç–∞—Ç—É—Å: –æ–¥–æ–±—Ä–µ–Ω–æ",
        "rejected": "‚ùå –°—Ç–∞—Ç—É—Å: –æ—Ç–∫–ª–æ–Ω–µ–Ω–æ",
    }
    return (
        f"üìå <b>–ó–∞–ø—Ä–æ—Å –Ω–∞ –≤—ã–≤–æ–¥ —Å—Ä–µ–¥—Å—Ç–≤ #{withdrawal_id}</b>\n\n"
        f"üë§ –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å: @{username} | ID {user_id}\n"
        f"üí´ –°—É–º–º–∞: {amount:.0f} ‚≠ê\n"
        f"{status_map.get(status, status)}"
    )


class WithdrawStates(StatesGroup):
    captcha = State()


def _gen_captcha() -> tuple[int, int]:
    a = random.randint(1, 19)
    b = random.randint(1, 20 - a)
    return a, b


